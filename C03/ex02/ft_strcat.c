char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	ja;

	i = 0;
	ja = 0;
	while (dest[i])
		i += 1;
	while (src[ja])
	{
		dest[i + ja] = src[ja];
		ja += 1;
	}
	dest[i + ja] = '\0';
	return (dest);
}
