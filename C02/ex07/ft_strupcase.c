char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'a' && 'b' >= str[i]))
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
