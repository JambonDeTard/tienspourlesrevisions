char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 'a' && 'z' >= str [i]) && i == 0)
			str[i] = str[i] - 32;
		if (!(str[i - 1] == ' ' || str[i - 1] == '+'\
				|| str[i - 1] == '-'\
				&& (str[i] >= 'a' && 'z' >= str[i])))
			str[i] = str[i] - 32;
		else
			str[i] = str[i] +32;
	}
	return (str);
}
